# https://www.hackerrank.com/challenges/python-arithmetic-operators/problem


def get_sum(a, b):
    print(a + b)


def get_difference(a, b):
    print(a - b)


def get_product(a, b):
    print(a * b)


if __name__ == "__main__":
    a = int(input())
    b = int(input())

    get_sum(a, b)
    get_difference(a, b)
    get_product(a, b)
